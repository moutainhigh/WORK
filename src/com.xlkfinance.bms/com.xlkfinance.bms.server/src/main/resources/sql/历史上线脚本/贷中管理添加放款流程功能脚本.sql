SET T.APPLY_STATUS=5,T.FUND_MANAGER_REMARK='旧数据，无审批意见',T.FINANCE_DIRECTOR_REMARK='旧数据，无审批意见' 
WHERE T.FINANCE_HANDLE_ID IN (SELECT FH.PID FROM BIZ_LOAN_FINANCE_HANDLE FH WHERE FH.STATUS=3);
