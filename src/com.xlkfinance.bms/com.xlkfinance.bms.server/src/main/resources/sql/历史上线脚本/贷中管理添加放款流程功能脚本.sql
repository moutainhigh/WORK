
 CREATE OR REPLACE VIEW CUS_ACCT_V AS
(
SELECT AC.PID         ACCT_ID,
       PS.CHINA_NAME  ACCT_NAME,
       AC.CUS_TYPE    ACCT_TYPE,
       AC.PM_USER_ID,
       AC.STATUS,
       AC.CUS_STATUS,
       NULL           ECO_TRADE,
       PS.TELEPHONE   TELEPHONE,
       PS.CERT_NUMBER AS CERT_NUMBER,
       PS.MAIL        MAIL,
       PS.UNIT_ADDR   ADDRESS,
       PS.UNIT_CODE   POSTAL_CODE
  FROM CUS_ACCT AC, CUS_PER_BASE PB, CUS_PERSON PS
 WHERE AC.PID = PB.ACCT_ID
   AND PB.ACCT_ID = PS.ACCT_ID
   AND PS.RELATION_TYPE = 1
UNION ALL
SELECT AC.PID,
       CB.CPY_NAME,
       AC.CUS_TYPE,
       AC.PM_USER_ID,
       AC.STATUS,
       AC.CUS_STATUS,
       CB.ECO_TRADE,
       CB.TELEPHONE,
       CB.BUS_LIC_CERT,
       CB.MAIL,
       CB.COMM_ADDR,
       CB.COMM_CODE
  FROM CUS_ACCT AC, CUS_COM_BASE CB
 WHERE AC.PID = CB.ACCT_ID
   AND AC.CUS_TYPE IN(2,3)
 );
